//-----------REQUIRE-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

const mongoose = require('mongoose');

//-----------SCHEMA------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

const poolTableSchema = new mongoose.Schema({
    id: Number,
    pool: String,
    teams: [
        {name: String, PL: Number, W: Number, D: Number, L: Number, PF: Number, PA: Number, DIFF: Number, TF: Number, BP: Number, POINTS: Number},
    ]
});

//-----------EXPORT------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

module.exports = mongoose.model('PoolTable', poolTableSchema);