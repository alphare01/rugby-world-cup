//-----------REQUIRE-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

const mongoose = require('mongoose');

//-----------SCHEMA------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

const poolMatchSchema = new mongoose.Schema({
    id: Number,
    pool: String,
    teamA: String,
    teamB: String,
    date: Date,
    place: String
});

//-----------EXPORT------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

module.exports = mongoose.model('PoolMatch', poolMatchSchema);